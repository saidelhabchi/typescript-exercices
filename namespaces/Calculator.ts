namespace Calculator {
    export class BasicCalculator {
        add(x:number,y:number): number {
            return x + y
        }
        sub(x:number,y:number): number {
            return x - y
        }
        div(x:number,y:number): number {
            return x / y
        }
        mult(x:number,y:number): number {
            return x * y
        }
    }      
}